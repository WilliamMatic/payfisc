// Générer des références aléatoires
const generateReference = (type: string, index: number): string => {
  const prefixes = {
    achat: "VIGN",
    delivrance: "DEL",
    renouvellement: "REN",
  };
  const prefix = prefixes[type as keyof typeof prefixes] || "VIGN";
  const date = Date.now() - index * 86400000;
  return `${prefix}-${date}-${Math.floor(Math.random() * 1000)}`;
};

// Générer des dates aléatoires sur les derniers mois
const generateDate = (monthsAgo: number): string => {
  const date = new Date();
  date.setMonth(date.getMonth() - monthsAgo);
  date.setHours(
    10 + Math.floor(Math.random() * 8),
    Math.floor(Math.random() * 60),
    Math.floor(Math.random() * 60),
  );
  return date.toISOString().replace("T", " ").substring(0, 19);
};

